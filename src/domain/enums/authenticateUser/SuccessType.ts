/**
 * Enum representing success types related to user authentication.
 * 
 */
enum AuthSuccessType {

    /**
     * Message indicating otp sent to the user's email for verification.
     */
    VerificationOtpSend = 'OTP sent to your email for verification.',

    /**
     * Message indication user is authenticated successfully.
     */
    AuthenticationSuccess = 'Authentication success',
}